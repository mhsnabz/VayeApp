package com.vaye.app.Model;

public class BolumModel {
    String key;
    String value;

    public BolumModel() {
    }

    public BolumModel(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
